package pis.pojo;

import java.io.Serializable;
import java.security.NoSuchAlgorithmException;
import java.security.spec.InvalidKeySpecException;

import pis.util.Factory;

/////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//
///////////////////////////////////////////////////////////////////////////////////////////////////////// 
public class User implements Serializable {
	/**
	 * Serialization Id
	 */
	private static final long serialVersionUID = 1L;

	String userId, actualPassword, hashedPassword, role, firstName, middleName, lastName, userEmail;
	boolean active;

	public User(String userId, String actualPassword, String firstName, String middleName, String lastName, String userEmail) throws NoSuchAlgorithmException, InvalidKeySpecException {
		super();
		this.userId = userId;
		this.actualPassword = actualPassword;
		this.hashedPassword = Factory.generateHash(null, actualPassword);
		this.firstName = firstName;
		this.middleName = middleName;
		this.lastName = lastName;
		this.userEmail = userEmail;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public User(String userId, String hashedPassword, String role, boolean active, String firstName, String middleName, String lastName, String userEmail) {
		super();
		this.userId = userId;
		this.hashedPassword = hashedPassword;
		this.role = role;
		this.active = active;
		this.firstName = firstName;
		this.middleName = middleName;
		this.lastName = lastName;
		this.userEmail = userEmail;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getUserId() {
		return userId;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setUserId(String userId) {
		this.userId = userId;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getHashedPassword() {
		return hashedPassword;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setHashedPassword(String hashedPassword) {
		this.hashedPassword = hashedPassword;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getName() {
		return firstName + " " + middleName + " " + lastName;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getFirstName() {
		return firstName;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getMiddleName() {
		return middleName;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getLastName() {
		return lastName;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public String getUserEmail() {
		return userEmail;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public boolean isActive() {
		return active;
	}

	public void setActive(boolean active) {
		this.active = active;
	}

	public String getActualPassword() {
		return actualPassword;
	}

	public void setActualPassword(String actualPassword) {
		this.actualPassword = actualPassword;
	}

	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	@Override
	public String toString() {
		return "User [userId=" + userId + ", actualPassword=" + actualPassword + ", hashedPassword=" + hashedPassword + ", role=" + role + ", firstName=" + firstName + ", middleName=" + middleName + ", lastName=" + lastName + ", userEmail=" + userEmail + ", active=" + active + "]";
	}

}
